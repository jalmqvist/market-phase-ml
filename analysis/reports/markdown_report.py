def _render_sentiment_comparison(
    lines: list[str], sentiment: dict[str, Any] | None
) -> None:
    lines.append("### Sentiment ON vs OFF (A/C vs B/D)\n")
    if not sentiment:
        lines.append("⚠ Sentiment comparison not computed.")
        lines.append("")
        return
    for w in sentiment.get("warnings") or []:
        lines.append(f"- ⚠ {w}")
    on_ids = ", ".join(sentiment.get("sentiment_on") or []) or "—"
    off_ids = ", ".join(sentiment.get("sentiment_off") or []) or "—"
    lines.append(f"- **Sentiment ON runs:** {on_ids}")
    lines.append(f"- **Sentiment OFF runs:** {off_ids}")
    lines.append(f"- **Valid comparisons:** {', '.join(sentiment.get('valid_comparisons') or []) or '—'}")
    lines.append(f"- **Incomplete comparisons:** {', '.join(sentiment.get('incomplete_comparisons') or []) or '—'}")
    lines.append(f"- **Invalid comparisons:** {', '.join(sentiment.get('invalid_comparisons') or []) or '—'}")
    lines.append("")
    delta_rows = sentiment.get("delta_table") or []
    if delta_rows:
        lines.append("**Delta Table (Sentiment ON − OFF):**\n")
        _table_from_rows(lines, delta_rows)
    else:
        lines.append("⚠ Delta table empty (need both ON and OFF runs).")
    lines.append("")


def _render_gen_comparison(
    lines: list[str], gen: dict[str, Any] | None
) -> None:
    lines.append("### Gen1 vs Gen2 (Missing-Indicator Semantics)\n")
    if not gen:
        lines.append("⚠ Gen1/Gen2 comparison not computed.")
        lines.append("")
        return
    for w in gen.get("warnings") or []:
        lines.append(f"- ⚠ {w}")
    g1_ids = ", ".join(gen.get("gen1") or []) or "—"
    g2_ids = ", ".join(gen.get("gen2") or []) or "—"
    lines.append(f"- **Gen1 runs:** {g1_ids}")
    lines.append(f"- **Gen2 runs:** {g2_ids}")
    lines.append(f"- **Valid comparisons:** {', '.join(gen.get('valid_comparisons') or []) or '—'}")
    lines.append(f"- **Incomplete comparisons:** {', '.join(gen.get('incomplete_comparisons') or []) or '—'}")
    lines.append(f"- **Invalid comparisons:** {', '.join(gen.get('invalid_comparisons') or []) or '—'}")
    lines.append("")
    delta_rows = gen.get("delta_table") or []
    if delta_rows:
        lines.append("**Delta Table (Gen2 − Gen1):**\n")
        _table_from_rows(lines, delta_rows)
    else:
        lines.append("⚠ Delta table empty (need both Gen1 and Gen2 runs).")
    lines.append("")


def _render_selector_aggregate(
    lines: list[str], selector: dict[str, Any] | None
) -> None:
    lines.append("### Selector Uplift Aggregate\n")
    if not selector:
        lines.append("⚠ Selector comparison not computed.")
        lines.append("")
        return
    for w in selector.get("warnings") or []:
        lines.append(f"- ⚠ {w}")
    agg = selector.get("aggregate") or {}
    if agg:
        lines.append("**Mean uplift per pair (Dynamic − Baseline):**\n")
        lines.append("| Pair | Sharpe Δ | Return Δ | MaxDD Δ |")
        lines.append("|------|----------|----------|---------|")
        for pair, metrics in sorted(agg.items()):
            sharpe = _fmt(metrics.get("Sharpe_Delta"))
            ret = _fmt(metrics.get("Return_Delta"))
            dd = _fmt(metrics.get("MaxDD_Delta"))
            lines.append(f"| {pair} | {sharpe} | {ret} | {dd} |")
    else:
        lines.append("⚠ No aggregate selector data available.")
    lines.append("")


def _render_manifest_metadata(lines: list[str], summary: dict[str, Any]) -> None:
    lines.append("### Manifest Metadata\n")
    meta = summary.get("meta") or {}
    md = meta.get("manifest_diagnostics") or {}
    reproducibility = meta.get("reproducibility") or {}
    feature_ordering = meta.get("feature_ordering") or {}
    lines.append(f"- Manifest present: {'yes' if meta.get('manifest_present') else 'no'}")
    lines.append(f"- Legacy mode: {'yes' if meta.get('legacy_mode') else 'no'}")
    lines.append(f"- Canonical manifest path: {md.get('manifest_path') or '—'}")
    lines.append(f"- Canonical manifest run_id: {md.get('manifest_run_id') or '—'}")
    lines.append(f"- Canonical manifest timestamp: {md.get('manifest_timestamp') or '—'}")
    lines.append(f"- Canonical manifest mode tag: {md.get('dl_mode_tag') or '—'}")
    lines.append(f"- Manifest count in run root: {md.get('manifest_count', 0)}")
    if reproducibility:
        lines.append("- Reproducibility seed metadata:")
        lines.append(f"  - experiment_seed: {reproducibility.get('experiment_seed', '—')}")
        lines.append(f"  - numpy_seed: {reproducibility.get('numpy_seed', '—')}")
        lines.append(f"  - python_random_seed: {reproducibility.get('python_random_seed', '—')}")
        lines.append(f"  - torch_seed: {reproducibility.get('torch_seed', '—')}")
    phase_predictor_pairs = sorted((feature_ordering.get("phase_predictor_by_pair") or {}).keys())
    selector_pairs = sorted((feature_ordering.get("strategy_selector_by_pair") or {}).keys())
    if phase_predictor_pairs or selector_pairs:
        lines.append("- Feature ordering metadata:")
        lines.append(f"  - dl_feature_columns: {', '.join(feature_ordering.get('dl_feature_columns') or []) or '—'}")
        lines.append(f"  - phase_predictor_by_pair: {', '.join(phase_predictor_pairs) or '—'}")
        lines.append(f"  - strategy_selector_by_pair: {', '.join(selector_pairs) or '—'}")
    lines.append("")


def _render_parser_diagnostics(lines: list[str], summary: dict[str, Any]) -> None:
    lines.append("### Parser Diagnostics\n")
    meta = summary.get("meta") or {}
    files_found = meta.get("files_found") or []
    lines.append(f"- Recognized CSV files: {len(files_found)}")
    if files_found:
        lines.append(f"- Files: {', '.join(sorted(files_found))}")
    else:
        lines.append("- Files: —")
    lines.append(f"- Log parsed: {'yes' if summary.get('log') else 'no'}")
    lines.append("")


def _render_integrity_section(
    lines: list[str],
    title: str,
    section: dict[str, Any] | None,
) -> None:
    lines.append(f"### {title}\n")
    if not section:
        lines.append("- No diagnostics.")
        lines.append("")
        return
    errors = section.get("errors") or []
    warnings = section.get("warnings") or []
    lines.append(f"- Errors: {len(errors)}")
    lines.append(f"- Warnings: {len(warnings)}")
    for err in errors:
        lines.append(f"  - ❌ {err}")
    for warning in warnings:
        lines.append(f"  - ⚠ {warning}")
    lines.append("")


