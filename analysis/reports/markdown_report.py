def _table_from_rows(
    lines: list[str],
    rows: list[dict[str, Any]],
    include: list[str] | None = None,
    exclude: set[str] | None = None,
) -> None:
    """Render *rows* as a markdown table appended to *lines*."""
    if not rows:
        lines.append("*(empty)*")
        return

    exclude = exclude or set()
    all_cols = list(rows[0].keys())

    if include:
        # Keep only columns that exist in the data and are in the include list
        cols = [c for c in include if c in all_cols]
        # If none matched, fall back to all
        if not cols:
            cols = [c for c in all_cols if c not in exclude]
    else:
        cols = [c for c in all_cols if c not in exclude]

    if not cols:
        lines.append("*(no columns)*")
        return

    header = "| " + " | ".join(str(c) for c in cols) + " |"
    sep = "|" + "|".join("---" for _ in cols) + "|"
    lines.append(header)
    lines.append(sep)
    for row in rows:
        cell = " | ".join(_fmt(row.get(c)) for c in cols)
        lines.append(f"| {cell} |")


def _fmt(val: Any) -> str:
    """Format a value for markdown table display."""
    if val is None:
        return "—"
    if isinstance(val, float):
        return f"{val:.4f}"
    return str(val)
